package ControlFlow;

public class If {
    public static void main(String[] args) {
        int x = 1;
        //Decision-Making
        if (x == 0) { //Boolean Expression
            System.out.println("X is 0");
        } else if (x == 1) {
            System.out.println("X is 1");
        } else {
            System.out.println("I don't know");
        }
    }
}
